bool canPartition(vector<int>& nums) {
    int sum = 0;
    for(int i = 0; i < nums.size(); i++) {
        sum = sum + nums[i];
    }
    int target = sum/2;
    if(sum%2 != 0) {
        return false;
    }
    set<int> dp;
    dp.insert(0);
    for(int i = 0; i < nums.size(); i++) {
        set<int> nextDP;
        for(auto temp : dp) {
            nextDP.insert(temp);
            nextDP.insert(temp + nums[i]);
        }
        dp = nextDP;
    }
    return dp.find(target) != dp.end();
}

